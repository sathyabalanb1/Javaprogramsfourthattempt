package streamapiprogramsvtwo;

public class Editor {
	
	private int age;
	
	private String name;
	
	public Editor(int age,String name) {
		this.age=age;
		this.name=name;
	}
	
	public int getAge() {
		return age;
	}
	
	public String getName() {
		return this.name;
	}
	
	public String toString() {
		return name;
	}

}
